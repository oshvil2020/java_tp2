
public class Item {
    /**
     * Position en 'x' de l'item dans l'univers de l'usine.
     */
    protected double x;
    /**
     * Position en 'y' de l'item dans l'univers de l'usine.
     */
    protected double y;

    public double getX() {
        return x;
    }

    public void setX( double x ) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY( double y ) {
        this.y = y;
    }
}
